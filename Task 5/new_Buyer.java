    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String name = name_text.getText();
        String contactNo = contact_text.getText();
        String email = email_text.getText();
        String address = address_text.getText();
        String gender = (String) gender_box.getSelectedItem();
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("insert into buyer values('" + name + "','" + contactNo + "','" + email + "','" + address + "','" + gender + "')");
            JOptionPane.showMessageDialog(null, "Successfully Updated");
            setVisible(false);
            new new_Buyer().setVisible(true);
        } catch (Exception e)
        {
            JOptionPane.showMessageDialog(null,"Mobile Number already exists.");
        }
    }//GEN-LAST:event_saveActionPerformed


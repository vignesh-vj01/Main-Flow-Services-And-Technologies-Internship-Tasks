    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        String pId = search_id.getText();
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select *from product where pId='" + pId + "'");
            if (rs.next()) {
                pname_text.setText(rs.getString(2));
                rate_text.setText(rs.getString(3));
                description_text.setText(rs.getString(4));
                activate_text.setText(rs.getString(5));
                search_id.setEditable(false);
            } else {
                JOptionPane.showMessageDialog(null, "Product ID does not Exist");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_searchActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        String pId = search_id.getText();
        String pname = pname_text.getText();
        String rate = rate_text.getText();
        String description = description_text.getText();
        String activate = activate_text.getText();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to update?", "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            try {
                Connection con = ConnectionProvider.getCon();
                Statement st = con.createStatement();
                st.executeUpdate("update product set pName='" + pname + "',rate='" + rate + "',description='" + description + "',activate='" + activate + "' where pId='" + pId + "'");
                setVisible(false);
                new update_Product().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_updateActionPerformed

